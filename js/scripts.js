///subtraction
      var subtract = function(number1, number2) {
        return number1 - number2;
      };

      var number1 = parseInt(prompt("Enter a number:"));
      var number2 = parseInt(prompt("Enter another number:"));
      var result = subtract(number1, number2);
      alert(result);

